use crate::ir;

pub struct RustGenerator;

impl RustGenerator {
    pub fn generate(&self, schema: &ir::Schema) -> String {
        let mut out = String::new();
        out.push_str("// Generated by kindc. DO NOT EDIT.\n");
        out.push_str("#![allow(dead_code)]\n");
        out.push_str("#![allow(non_camel_case_types)]\n\n");
        out.push_str("extern crate alloc;\n\n");
        out.push_str("use alloc::string::String;\n");
        out.push_str("use alloc::vec::Vec;\n");
        out.push_str("// FIXME: This should be imported from abi::wire\n");
        out.push_str("// use kind_abi::wire::ThingId;\n");
        out.push_str("#[repr(C)]\n");
        out.push_str("#[derive(Copy, Clone, Debug, PartialEq, Eq, Default)]\n");
        out.push_str("pub struct ThingId(pub [u8; 16]);\n\n");

        // Group by modules if we wanted, but for v1 we do flat with prefixes or just last segment
        // Let's do a single module for now but we'll collect all kinds and sort them
        let mut sorted_kinds: Vec<_> = schema.kinds.values().collect();
        sorted_kinds.sort_by_key(|k| &k.canonical_name);

        for kind in sorted_kinds {
            if let ir::KindShape::Builtin(_) = &kind.shape {
                continue;
            }

            if let Some(doc) = &kind.doc {
                for line in doc.lines() {
                    out.push_str(&format!("/// {}\n", line));
                }
            }

            let rust_name = self.to_pascal_case(&kind.canonical_name);
            
            // Generate KIND_ID
            let id_name = format!("KIND_ID_{}", kind.canonical_name.replace(".", "_").to_uppercase());
            out.push_str(&format!("pub const {}: [u8; 16] = [", id_name));
            for (i, byte) in kind.kind_id.iter().enumerate() {
                out.push_str(&format!("0x{:02x}", byte));
                if i < 15 {
                    out.push_str(", ");
                }
            }
            out.push_str("];\n\n");

            match &kind.shape {
                ir::KindShape::Struct(fields) => {
                    out.push_str("#[repr(C)]\n");
                    out.push_str("#[derive(Clone, Debug, PartialEq, Eq)]\n");
                    out.push_str(&format!("pub struct {} {{\n", rust_name));
                    for f in fields {
                        if let Some(doc) = &f.doc {
                            for line in doc.lines() {
                                out.push_str(&format!("    /// {}\n", line));
                            }
                        }
                        out.push_str(&format!("    pub {}: {},\n", f.name, self.map_type(&f.ty)));
                    }
                    out.push_str("}\n\n");
                }
                ir::KindShape::Enum(variants) => {
                    out.push_str("#[derive(Clone, Debug, PartialEq, Eq)]\n");
                    out.push_str(&format!("pub enum {} {{\n", rust_name));
                    for v in variants {
                        if let Some(doc) = &v.doc {
                            for line in doc.lines() {
                                out.push_str(&format!("    /// {}\n", line));
                            }
                        }
                        match &v.payload {
                            ir::ResolvedVariantPayload::Unit => {
                                out.push_str(&format!("    {},\n", v.name));
                            }
                            ir::ResolvedVariantPayload::Tuple(ty) => {
                                out.push_str(&format!("    {}({}),\n", v.name, self.map_type(ty)));
                            }
                            ir::ResolvedVariantPayload::Struct(fields) => {
                                out.push_str(&format!("    {} {{\n", v.name));
                                for f in fields {
                                    if let Some(doc) = &f.doc {
                                        for line in doc.lines() {
                                            out.push_str(&format!("        /// {}\n", line));
                                        }
                                    }
                                    out.push_str(&format!("        {}: {},\n", f.name, self.map_type(&f.ty)));
                                }
                                out.push_str("    },\n");
                            }
                        }
                    }
                    out.push_str("}\n\n");
                }
                ir::KindShape::Alias(ty) => {
                    out.push_str(&format!("pub type {} = {};\n\n", rust_name, self.map_type(ty)));
                }
                _ => {}
            }
        }

        out
    }

    fn to_pascal_case(&self, s: &str) -> String {
        let segments: Vec<&str> = s.split('.').collect();
        let last = *segments.last().unwrap_or(&s);
        
        // If the last segment is a generic suffix, prepend the parent segment
        let name_to_use = if segments.len() > 1 && matches!(last, "kind" | "state" | "args" | "result") {
            let parent = segments[segments.len() - 2];
            format!("{}_{}", parent, last)
        } else {
            last.to_string()
        };

        let mut result = String::new();
        let mut capitalize_next = true;
        for c in name_to_use.chars() {
            if c == '_' {
                capitalize_next = true;
            } else if capitalize_next {
                result.push(c.to_ascii_uppercase());
                capitalize_next = false;
            } else {
                result.push(c);
            }
        }
        result
    }

    fn map_type(&self, ty: &ir::ResolvedType) -> String {
        match ty.kind_ref.as_str() {
            "bool" => "bool".to_string(),
            "u8" => "u8".to_string(),
            "u16" => "u16".to_string(),
            "u32" => "u32".to_string(),
            "u64" => "u64".to_string(),
            "i8" => "i8".to_string(),
            "i16" => "i16".to_string(),
            "i32" => "i32".to_string(),
            "i64" => "i64".to_string(),
            "string" => "String".to_string(),
            "bytes" => "Vec<u8>".to_string(),
            "list" => format!("Vec<{}>", self.map_type(&ty.args[0])),
            "option" => format!("Option<{}>", self.map_type(&ty.args[0])),
            "result" => format!("Result<{}, {}>", self.map_type(&ty.args[0]), self.map_type(&ty.args[1])),
            "ref" => "ThingId".to_string(),
            _ => self.to_pascal_case(&ty.kind_ref),
        }
    }
}
